    private void button_resultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_resultadoActionPerformed
        //igual
        double segundonumero=0;
        if (operacion.equals("nula")) {
            labelprocess.setText(cadena);
        }else
        if (operacion=="sumar") {
            segundonumero=Double.parseDouble(cadena);
            resultado=primerNumero+segundonumero;
            labelprocess.setText(String.format("% .2f", resultado));
        cadena=String.valueOf(resultado);
       
        operacion="nula";
        }else
        if (operacion=="restar") {
            segundonumero=Double.parseDouble(cadena);
            resultado=primerNumero-segundonumero;
            labelprocess.setText(String.format("% .2f", resultado));
        cadena=String.valueOf(resultado);
        }else
        if (operacion=="multiplicar") {
            segundonumero=Double.parseDouble(cadena);
            resultado=primerNumero*segundonumero;
            labelprocess.setText(String.format("% .2f", resultado));
        cadena=String.valueOf(resultado);
        }else
        if (operacion=="dividir") {
            segundonumero=Double.parseDouble(cadena);
            if (segundonumero==0) {
                JOptionPane.showInternalMessageDialog(null, "No se puede dividir entre 0");
            }else{
                  resultado=primerNumero/segundonumero;
            labelprocess.setText(String.format("% .2f", resultado));
        cadena=String.valueOf(resultado);
            }
        }else
        if (operacion=="porciento") {
    
               segundonumero=Double.parseDouble(cadena);
            resultado=(primerNumero/100)*segundonumero;
            labelprocess.setText(String.format("% .0f", resultado));
        cadena=String.valueOf(resultado);
            
        }
        operacion="nula";
         
        labelresult.setText("");
        activado=true;
       
          
    }//GEN-LAST:event_button_resultadoActionPerformed

